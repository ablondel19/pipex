#include "pipex.h"

char	*ft_strjoin(char *s1, char *s2)
{
	char			*dst;
	unsigned int	i;

	if (!s1 || !s2)
		return (NULL);
	dst = ft_memzero(ft_len(s1) + ft_len(s2));
	if (!dst)
		return (NULL);
	i = 0;
	while (*s1)
		dst[i++] = *s1++;
	while (*s2)
		dst[i++] = *s2++;
	dst[i] = '\0';
	return (dst);
}
